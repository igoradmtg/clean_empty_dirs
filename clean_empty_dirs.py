#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
–°–∫—Ä–∏–ø—Ç –¥–ª—è –æ—á–∏—Å—Ç–∫–∏ –ø—É—Å—Ç—ã—Ö –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–π.

–≠—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ –æ–±—Ö–æ–¥–∏—Ç —É–∫–∞–∑–∞–Ω–Ω—ã–µ –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏
–¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –∏ —É–¥–∞–ª—è–µ—Ç –≤—Å–µ –≤–ª–æ–∂–µ–Ω–Ω—ã–µ –ø–∞–ø–∫–∏, –∫–æ—Ç–æ—Ä—ã–µ –Ω–µ —Å–æ–¥–µ—Ä–∂–∞—Ç —Ñ–∞–π–ª–æ–≤ –∏–ª–∏
–¥—Ä—É–≥–∏—Ö –ø–∞–ø–æ–∫.

–û—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏:
  - –ü—Ä–∏–Ω–∏–º–∞–µ—Ç –æ–¥–∏–Ω –∏–ª–∏ –Ω–µ—Å–∫–æ–ª—å–∫–æ –ø—É—Ç–µ–π –∫ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è–º –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤.
  - –ò—Å–ø–æ–ª—å–∑—É–µ—Ç –±–µ–∑–æ–ø–∞—Å–Ω—ã–π –æ–±—Ö–æ–¥ —Ñ–∞–π–ª–æ–≤–æ–π —Å–∏—Å—Ç–µ–º—ã "—Å–Ω–∏–∑—É –≤–≤–µ—Ä—Ö" (`os.walk(topdown=False)`),
    —á—Ç–æ –ø–æ–∑–≤–æ–ª—è–µ—Ç –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ —É–¥–∞–ª—è—Ç—å —Å–Ω–∞—á–∞–ª–∞ –¥–æ—á–µ—Ä–Ω–∏–µ –ø—É—Å—Ç—ã–µ –ø–∞–ø–∫–∏, –∞ –∑–∞—Ç–µ–º
    –∏—Ö —Ä–æ–¥–∏—Ç–µ–ª—å—Å–∫–∏–µ, –µ—Å–ª–∏ –æ–Ω–∏ —Ç–æ–∂–µ —Å—Ç–∞–Ω–æ–≤—è—Ç—Å—è –ø—É—Å—Ç—ã–º–∏.
  - –í—ã–≤–æ–¥–∏—Ç –ø–æ–¥—Ä–æ–±–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø—Ä–æ—Ü–µ—Å—Å–µ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è, –≤–∫–ª—é—á–∞—è –∫–∞–∂–¥—É—é
    —É–¥–∞–ª–µ–Ω–Ω—É—é –ø–∞–ø–∫—É.
  - –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –≤–æ–∑–º–æ–∂–Ω—ã–µ –æ—à–∏–±–∫–∏, –Ω–∞–ø—Ä–∏–º–µ—Ä, –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–µ –ø—Ä–∞–≤ –¥–æ—Å—Ç—É–ø–∞ –Ω–∞ —É–¥–∞–ª–µ–Ω–∏–µ
    –ø–∞–ø–∫–∏.
  - –ü–ª–∞—Ç—Ñ–æ—Ä–º–æ–Ω–µ–∑–∞–≤–∏—Å–∏–º—ã–π, –±—É–¥–µ—Ç —Ä–∞–±–æ—Ç–∞—Ç—å –≤ Windows, macOS –∏ Linux.

–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ:
  python clean_empty_dirs.py "/–ø—É—Ç—å/–∫/–ø–∞–ø–∫–µ1" "C:/–ø—É—Ç—å/–∫/–ø–∞–ø–∫–µ2"

"""

import os      # –ú–æ–¥—É–ª—å –¥–ª—è –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏—è —Å –æ–ø–µ—Ä–∞—Ü–∏–æ–Ω–Ω–æ–π —Å–∏—Å—Ç–µ–º–æ–π, –Ω–∞–ø—Ä–∏–º–µ—Ä, –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –ø—É—Ç—è–º–∏ –∏ –ø–∞–ø–∫–∞–º–∏.
import sys     # –ú–æ–¥—É–ª—å –¥–ª—è –¥–æ—Å—Ç—É–ø–∞ –∫ —Å–∏—Å—Ç–µ–º–Ω—ã–º –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º, –≤ —á–∞—Å—Ç–Ω–æ—Å—Ç–∏, –∫ –∞—Ä–≥—É–º–µ–Ω—Ç–∞–º –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏.

def delete_empty_folders(path):
    """
    –†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –Ω–∞—Ö–æ–¥–∏—Ç –∏ —É–¥–∞–ª—è–µ—Ç –≤—Å–µ –ø—É—Å—Ç—ã–µ –ø–æ–¥–ø–∞–ø–∫–∏ –≤ —É–∫–∞–∑–∞–Ω–Ω–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏.

    Args:
        path (str): –°—Ç—Ä–æ–∫–∞ —Å –∞–±—Å–æ–ª—é—Ç–Ω—ã–º –∏–ª–∏ –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω—ã–º –ø—É—Ç–µ–º –∫ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏,
                    –∫–æ—Ç–æ—Ä—É—é –Ω—É–∂–Ω–æ –æ—á–∏—Å—Ç–∏—Ç—å.
    """
    # –ü–µ—Ä–≤—ã–º –¥–µ–ª–æ–º –ø—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã–π –ø—É—Ç—å –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω–æ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –∏ —è–≤–ª—è–µ—Ç—Å—è –ø–∞–ø–∫–æ–π.
    # –≠—Ç–æ –±–∞–∑–æ–≤–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞, —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –æ—à–∏–±–æ–∫ –Ω–∞ —Å—Ç–∞—Ä—Ç–µ.
    if not os.path.isdir(path):
        print(f"‚ùå –û—à–∏–±–∫–∞: –ü—É—Ç—å '{path}' –Ω–µ –Ω–∞–π–¥–µ–Ω –∏–ª–∏ –Ω–µ —è–≤–ª—è–µ—Ç—Å—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–µ–π.")
        return # –ï—Å–ª–∏ –ø—É—Ç—å –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–µ–Ω, –ø—Ä–µ–∫—Ä–∞—â–∞–µ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏.

    print(f"üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏: {path}\n" + "-"*30)

    # –°—á–µ—Ç—á–∏–∫ –¥–ª—è —É–¥–∞–ª–µ–Ω–Ω—ã—Ö –ø–∞–ø–æ–∫. –ü–æ–ª–µ–∑–Ω–æ –¥–ª—è –∏—Ç–æ–≥–æ–≤–æ–π —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏.
    deleted_count = 0

    # os.walk() - —ç—Ç–æ –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä, –∫–æ—Ç–æ—Ä—ã–π "–ø—Ä–æ–≥—É–ª–∏–≤–∞–µ—Ç—Å—è" –ø–æ –¥–µ—Ä–µ–≤—É –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–π.
    # –û–Ω –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–æ—Ä—Ç–µ–∂–∏ (—Ç–µ–∫—É—â–∏–π_–ø—É—Ç—å, —Å–ø–∏—Å–æ–∫_–ø–æ–¥–ø–∞–ø–æ–∫, —Å–ø–∏—Å–æ–∫_—Ñ–∞–π–ª–æ–≤).
    # topdown=False - –∫–ª—é—á–µ–≤–æ–π –ø–∞—Ä–∞–º–µ—Ç—Ä! –û–Ω –∑–∞—Å—Ç–∞–≤–ª—è–µ—Ç –æ–±—Ö–æ–¥ –∏–¥—Ç–∏ "—Å–Ω–∏–∑—É –≤–≤–µ—Ä—Ö",
    # –æ—Ç —Å–∞–º—ã—Ö –≥–ª—É–±–æ–∫–∏—Ö –ø–∞–ø–æ–∫ –∫ –∫–æ—Ä–Ω–µ–≤–æ–π. –≠—Ç–æ –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç, —á—Ç–æ –º—ã —Å–Ω–∞—á–∞–ª–∞
    # —É–¥–∞–ª–∏–º –ø—É—Å—Ç—É—é –ø–∞–ø–∫—É 'c' –≤ –ø—É—Ç–∏ 'a/b/c', –∏ —Ç–æ–ª—å–∫–æ –ø–æ—Ç–æ–º 'b' —Å–º–æ–∂–µ—Ç —Å—Ç–∞—Ç—å –ø—É—Å—Ç–æ–π.
    for dirpath, dirnames, filenames in os.walk(path, topdown=False):
        # –£—Å–ª–æ–≤–∏–µ –ø—Ä–æ—Å—Ç–æ–µ: –µ—Å–ª–∏ —Å–ø–∏—Å–æ–∫ –ø–æ–¥–ø–∞–ø–æ–∫ (dirnames) –∏ —Å–ø–∏—Å–æ–∫ —Ñ–∞–π–ª–æ–≤ (filenames) –ø—É—Å—Ç,
        # —Ç–æ —Ç–µ–∫—É—â–∞—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è (dirpath) —è–≤–ª—è–µ—Ç—Å—è –ø—É—Å—Ç–æ–π.
        if not dirnames and not filenames:
            # –ò—Å–ø–æ–ª—å–∑—É–µ–º –±–ª–æ–∫ try...except –¥–ª—è "–±–µ–∑–æ–ø–∞—Å–Ω–æ–≥–æ" —É–¥–∞–ª–µ–Ω–∏—è.
            # –ï—Å–ª–∏ –≤–æ–∑–Ω–∏–∫–Ω–µ—Ç –æ—à–∏–±–∫–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, –Ω–µ—Ç –ø—Ä–∞–≤), –ø—Ä–æ–≥—Ä–∞–º–º–∞ –Ω–µ "—É–ø–∞–¥–µ—Ç".
            try:
                # os.rmdir() —É–¥–∞–ª—è–µ—Ç –ø—É—Å—Ç—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é.
                os.rmdir(dirpath)
                # –í—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± —É—Å–ø–µ—à–Ω–æ–º —É–¥–∞–ª–µ–Ω–∏–∏.
                print(f"‚úÖ –£–¥–∞–ª–µ–Ω–∞ –ø—É—Å—Ç–∞—è –ø–∞–ø–∫–∞: {dirpath}")
                # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á–µ—Ç—á–∏–∫ —É–¥–∞–ª–µ–Ω–Ω—ã—Ö –ø–∞–ø–æ–∫.
                deleted_count += 1
            except OSError as e:
                # OSError - —ç—Ç–æ –æ–±—â–µ–µ –∏—Å–∫–ª—é—á–µ–Ω–∏–µ –¥–ª—è –æ—à–∏–±–æ–∫ –≤–≤–æ–¥–∞-–≤—ã–≤–æ–¥–∞.
                # –í—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ, —á—Ç–æ–±—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∑–Ω–∞–ª, —á—Ç–æ –ø–æ—à–ª–æ –Ω–µ —Ç–∞–∫.
                print(f"‚ùó –ù–µ —É–¥–∞–ª–æ—Å—å —É–¥–∞–ª–∏—Ç—å '{dirpath}': {e}")

    # –ü–æ—Å–ª–µ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è —Ü–∏–∫–ª–∞ –≤—ã–≤–æ–¥–∏–º –∏—Ç–æ–≥–æ–≤—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é.
    if deleted_count == 0:
        print("–ü—É—Å—Ç—ã—Ö –ø–∞–ø–æ–∫ –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è –Ω–µ –Ω–∞–π–¥–µ–Ω–æ.")
    else:
        print(f"\nüéâ –ì–æ—Ç–æ–≤–æ! –í—Å–µ–≥–æ —É–¥–∞–ª–µ–Ω–æ –ø—É—Å—Ç—ã—Ö –ø–∞–ø–æ–∫: {deleted_count}")
    # –î–æ–±–∞–≤–ª—è–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É –¥–ª—è –ª—É—á—à–µ–π —á–∏—Ç–∞–µ–º–æ—Å—Ç–∏ –≤—ã–≤–æ–¥–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –Ω–µ—Å–∫–æ–ª—å–∫–∏—Ö –ø–∞–ø–æ–∫.
    print("\n")


# __name__ == "__main__" - —ç—Ç–æ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–∞—è —Ç–æ—á–∫–∞ –≤—Ö–æ–¥–∞ –≤ Python.
# –ö–æ–¥ –≤–Ω—É—Ç—Ä–∏ —ç—Ç–æ–≥–æ –±–ª–æ–∫–∞ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è —Ç–æ–ª—å–∫–æ —Ç–æ–≥–¥–∞, –∫–æ–≥–¥–∞ —Ñ–∞–π–ª –∑–∞–ø—É—â–µ–Ω –Ω–∞–ø—Ä—è–º—É—é,
# –∞ –Ω–µ –∏–º–ø–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω –∫–∞–∫ –º–æ–¥—É–ª—å –≤ –¥—Ä—É–≥–æ–π —Å–∫—Ä–∏–ø—Ç.
if __name__ == "__main__":
    # sys.argv - —ç—Ç–æ —Å–ø–∏—Å–æ–∫ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏.
    # sys.argv[0] - –∏–º—è —Å–∫—Ä–∏–ø—Ç–∞.
    # sys.argv[1:] - –≤—Å–µ –ø–æ—Å–ª–µ–¥—É—é—â–∏–µ –∞—Ä–≥—É–º–µ–Ω—Ç—ã.
    # –ï—Å–ª–∏ –¥–ª–∏–Ω–∞ —Å–ø–∏—Å–∫–∞ –º–µ–Ω—å—à–µ 2, –∑–Ω–∞—á–∏—Ç, –Ω–µ –ø–µ—Ä–µ–¥–∞–Ω–æ –Ω–∏ –æ–¥–Ω–æ–≥–æ –∞—Ä–≥—É–º–µ–Ω—Ç–∞ —Å –ø—É—Ç–µ–º.
    if len(sys.argv) < 2:
        # –í—ã–≤–æ–¥–∏–º –ø–æ–¥—Ä–æ–±–Ω—É—é –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—é –ø–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—é.
        print("\n–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: python clean_empty_dirs.py <–ø—É—Ç—å_–∫_–ø–∞–ø–∫–µ1> [<–ø—É—Ç—å_–∫_–ø–∞–ø–∫–µ2> ...]")
        print("\n–°–∫—Ä–∏–ø—Ç —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ —É–¥–∞–ª—è–µ—Ç –≤—Å–µ –ø—É—Å—Ç—ã–µ –ø–æ–¥–ø–∞–ø–∫–∏ –≤ —É–∫–∞–∑–∞–Ω–Ω—ã—Ö –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è—Ö.")
        print("\n–ü—Ä–∏–º–µ—Ä:")
        print('  python clean_empty_dirs.py "C:/Users/Admin/Downloads" "/mnt/data/images"')
        # sys.exit(1) –∑–∞–≤–µ—Ä—à–∞–µ—Ç –ø—Ä–æ–≥—Ä–∞–º–º—É —Å –∫–æ–¥–æ–º –æ—à–∏–±–∫–∏, —Å–∏–≥–Ω–∞–ª–∏–∑–∏—Ä—É—è –æ –Ω–µ—É–¥–∞—á–Ω–æ–º –∑–∞–ø—É—Å–∫–µ.
        sys.exit(1)

    # –ü–æ–ª—É—á–∞–µ–º —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤, –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã—Ö –ø–æ—Å–ª–µ –∏–º–µ–Ω–∏ —Å–∫—Ä–∏–ø—Ç–∞.
    dirs_from_args = sys.argv[1:]

    # –ó–∞–ø—É—Å–∫–∞–µ–º –Ω–∞—à—É –æ—Å–Ω–æ–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é –≤ —Ü–∏–∫–ª–µ –¥–ª—è –∫–∞–∂–¥–æ–π –ø–µ—Ä–µ–¥–∞–Ω–Ω–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏.
    for directory in dirs_from_args:
        delete_empty_folders(directory)
